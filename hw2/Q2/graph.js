  var nodes = {};

  // Compute the distinct nodes from the links.
  links.forEach(function(link) {
      link.source = nodes[link.source] ||
          (nodes[link.source] = {name: link.source});
      link.target = nodes[link.target] ||
          (nodes[link.target] = {name: link.target});
  });

  var width = 1200,
      height = 700,
      color = d3.scale.category20c();


  var force = d3.layout.force()
      .nodes(d3.values(nodes))
      .links(links)
      .size([width, height])
      .linkDistance(60)
      .charge(-250)
      .on("tick", tick)
      .start();

  // Set the range
  var  v = d3.scale.linear().range([0, 100]);

  // Scale the range of the data
  v.domain([0, d3.max(links, function(d) { return d.value; })]);


  var svg = d3.select("body").append("svg")
      .attr("width", width)
      .attr("height", height);

  // build the arrow.
  svg.append("svg:defs").selectAll("marker")
      .data(["end"])      // Different link/path types can be defined here
    .enter().append("svg:marker")    // This section adds in the arrows
      .attr("id", String)
      .attr("viewBox", "0 -5 10 10")
      .attr("refX", 15)
      .attr("refY", -1.5)
      .attr("markerWidth", 6)
      .attr("markerHeight", 6)
      .attr("orient", "auto")
    .append("svg:path")
      .attr("d", "M0,-5L10,0L0,5");

  // add the links and the arrows
  var path = svg.append("svg:g").selectAll("path")
      .data(force.links())
    .enter().append("svg:path")
      .attr("class", function(d) { return "link " + d.type; })
      .style("stroke",function(d){
        if(d.value==0){
          return "blue"
        }
        else if (d.value==1) {
          return "red"
        }
      });

  // define the nodes
  var node = svg.selectAll(".node")
      .data(force.nodes())
    .enter().append("g")
      .attr("class", "node")
      .on("dblclick", dblclick)
      .call(force.drag);

  // add the nodes
  // Remember you can always get the degree of the node using .weight
    node.append("circle")
      .attr("r",function(d){
        //console.log(d.weight)
        return Math.sqrt(d.weight)*6
      });



  // adding labels to the node
  node.append("svg:text")
      .attr("x", 8)
      .attr("y", ".31em")
      .text(function(d) { return d.name });


function dblclick(d){
    if (d.fixed == false){
      d3.select(this).select("circle").classed("fixed", d.fixed = true)
    }
    else if (d.fixed == true) {
     d3.select(this).select("circle").classed("fixed", d.fixed = false)
    }
  }

// add the curvy lines
function tick() {
    path.attr("d", function(d) {
        var dx = d.target.x - d.source.x,
            dy = d.target.y - d.source.y,
            dr = Math.sqrt(dx * dx + dy * dy);
        return "M" +
            d.source.x + "," +
            d.source.y + "A" +
            dr + "," + dr + " 0 0,1 " +
            d.target.x + "," +
            d.target.y;
    });

    node
        .attr("transform", function(d) {
		    return "translate(" + d.x + "," + d.y + ")"; });
};
